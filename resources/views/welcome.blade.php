                  2. Mandikan anak setiap hari pagi dan sore pakai sabun mandi. <br>
                  3. Cuci rambut dengan shampo 2-3 kali dalam satu minggu. <br>
                  4. Cuci tangan anak dengan sabun sebelum makan dan sesudah buang air besar. <br>
                  5. Bersihkan rumah setiap hari dari sampah dan genangan air. <br>
                </p>
