                  3. Pada umur 2 tahun ajari anak gosok gigi sendiri. <br>
                  4. Tidak membiasakan anak makan makanan yang manis dan lengket. <br>
                  5. Periksakan kesehatan gigi anak setiap 6 bulan setelah anak berumur 2 tahun. <br>
